#!/usr/bin/env python3
"""concat: function that takes a string str1 and a string str2 as arguments
and returns a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """Returns a concatenated string using the arguments
        Args:
            str1 (str): first string argument
            str2 (str): second string argument
    """
    return str1 + str2
